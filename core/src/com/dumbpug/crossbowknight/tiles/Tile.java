package com.dumbpug.crossbowknight.tiles;


/**
 * Represents a world tile.
 * @author nikolas.howard
 */
public class Tile {
	/** X/Y position of this tile. */
	private int x;
	private int y;
	
	/**
	 * Get the x position of this tile.
	 * @return x position.
	 */
	public int getX() {
		return x;
	}
	
	/**
	 * Get the y position of this tile.
	 * @return y position.
	 */
	public int getY() {
		return y;
	}
}
